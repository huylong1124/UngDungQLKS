    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed

        String maKH = txtTimKiem.getText().trim();

        if (maKH.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên nhân viên cần tìm!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            // Sử dụng constructor trống để tránh kiểm tra các giá trị không cần thiết
            QLKhachHang qlkh = new QLKhachHang();
            DefaultTableModel model = qlkh.timKiemKhachHang(maKH);
            tableQLKhachHang.setModel(model); // Cập nhật JTable với kết quả tìm kiếm
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

