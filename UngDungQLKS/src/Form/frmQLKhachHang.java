    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // Lấy dòng đã chọn trong JTable
        int selectedRow = tableQLKhachHang.getSelectedRow();

        // Kiểm tra xem có dòng nào được chọn không
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhân viên cần sửa!");
        } else {
            // Lấy giá trị của từng cột từ hàng được chọn trong JTable
            String makh = tableQLKhachHang.getValueAt(selectedRow, 0).toString();
            String hoten = tableQLKhachHang.getValueAt(selectedRow, 1).toString();
            String socmnd = tableQLKhachHang.getValueAt(selectedRow, 2).toString();
            String sdt = tableQLKhachHang.getValueAt(selectedRow, 3).toString();
            String diaChi = tableQLKhachHang.getValueAt(selectedRow, 4).toString();

            // Hiển thị dữ liệu lên các JTextField để người dùng có thể chỉnh sửa
            txtMaKH.setText(makh);
            txtTenKH.setText(hoten);
            txtCMND.setText(socmnd);
            txtSDT.setText(sdt);
            txtDiaChi.setText(diaChi);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

