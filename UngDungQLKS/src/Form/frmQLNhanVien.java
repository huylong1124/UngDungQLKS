    public void loadDataIntoTable() {
        Connection conn = getConnection();
        DefaultTableModel model = (DefaultTableModel) tableQLNhanVien.getModel();  // tableQLNhanVien là tên JTable
        model.setRowCount(0);  // Xóa dữ liệu cũ trong bảng (nếu có)

        try {
            String sql = "SELECT MaNV, HoTen, ChucVu, SDT, DiaChi, HeSoLuong FROM dbo.NhanVien";
            var ps = conn.prepareStatement(sql);
            var rs = ps.executeQuery();

            // Đổ dữ liệu vào JTable
            while (rs.next()) {
                String maNV = rs.getString("MaNV");
                String hoten = rs.getString("HoTen"); 
                String chucVu = rs.getString("ChucVu");
                String sdt = rs.getString("SDT");
                String diaChi = rs.getString("DiaChi");        
                String heSoLuong = rs.getString("HeSoLuong");           

                // Thêm dữ liệu vào model của JTable
                model.addRow(new Object[]{maNV, hoten, chucVu, sdt, diaChi,  heSoLuong});
            }

            rs.close();
            ps.close();
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // Lấy dòng đã chọn trong JTable
        int selectedRow = tableQLNhanVien.getSelectedRow();

        // Kiểm tra xem có dòng nào được chọn không
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhân viên cần xóa!");
        } else {
            // Lấy giá trị MaNV từ dòng đã chọn (cột MaNV ở vị trí 0)
            String maNV = tableQLNhanVien.getValueAt(selectedRow, 0).toString();

            // Xác nhận trước khi xóa
            int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa nhân viên có mã: " + maNV + "?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Thực hiện xóa nhân viên trong cơ sở dữ liệu
                Connection conn = getConnection();
                try {
                    String sql = "DELETE FROM dbo.NhanVien WHERE MaNV = ?";
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setString(1, maNV);  // Gán giá trị MaNV vào câu lệnh SQL

                    int rowsDeleted = ps.executeUpdate();  // Thực thi lệnh xóa
                    if (rowsDeleted > 0) {
                        JOptionPane.showMessageDialog(null, "Xóa nhân viên thành công!");                  
                        loadDataIntoTable();  // Cập nhật lại JTable sau khi xóa
                    } else {
                        JOptionPane.showMessageDialog(null, "Xóa nhân viên thất bại!");
                    }

                    ps.close();
                    conn.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Có lỗi xảy ra khi xóa dữ liệu!");
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

