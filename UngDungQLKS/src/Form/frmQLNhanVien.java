    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // Lấy dòng đã chọn trong JTable
        int selectedRow = tableQLNhanVien.getSelectedRow();

        // Kiểm tra xem có dòng nào được chọn không
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn nhân viên cần sửa!");
        } else {
            // Lấy giá trị của từng cột từ hàng được chọn trong JTable
            String maNV = tableQLNhanVien.getValueAt(selectedRow, 0).toString();
            String hoTen = tableQLNhanVien.getValueAt(selectedRow, 1).toString();
            String chucVu = tableQLNhanVien.getValueAt(selectedRow, 2).toString();
            String sdt = tableQLNhanVien.getValueAt(selectedRow, 3).toString();
            String diaChi = tableQLNhanVien.getValueAt(selectedRow, 4).toString();       
            String heSoLuong = tableQLNhanVien.getValueAt(selectedRow, 5).toString();          

            // Hiển thị dữ liệu lên các JTextField để người dùng có thể chỉnh sửa
            txtMaNV.setText(maNV);
            txtTenNV.setText(hoTen);
            CBbChucVu.setSelectedItem(chucVu);
            txtSDT.setText(sdt);
            txtDiaChi.setText(diaChi);
           
            txtHeSoLuong.setText(heSoLuong);          
        }
    }//GEN-LAST:event_btnSuaActionPerformed

